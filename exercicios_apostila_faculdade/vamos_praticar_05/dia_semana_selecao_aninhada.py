numero = int(input('Escolha um número de 1 a 7: '))
dia = ''

if numero == 1:
    dia = 'domingo'
elif numero == 2:
    dia = 'segunda'
elif numero == 3:
    dia = 'terça'
elif numero == 4:
    dia = 'quarta'
elif numero == 5:
    dia = 'quinta'
elif numero == 6:
    dia = 'sexta'
elif numero == 7:
    dia = 'sábado'

print(dia)
