numero = int(input('Digite um número de 1 a 7: '))
dia = ''

if numero == 1:
    dia = 'domingo'
if numero == 2:
    dia = 'segunda'
if numero == 3:
    dia = 'terca'
if numero == 4:
    dia = 'quarta'
if numero == 5:
    dia = 'quinta'
if numero == 6:
    dia = 'sexta'
if numero == 7:
    dia = 'sábado'

print(dia)
